// 1. Find the absolute value of -45
console.log(Math.abs(-45));  
// Output: 45

// 2. Round 12.78 to the nearest whole number
console.log(Math.round(12.78));  
// Output: 13

// 3. Use Math.ceil() to round up 9.23
console.log(Math.ceil(9.23));  
// Output: 10

// 4. Use Math.floor() to round down 6.99
console.log(Math.floor(6.99));  
// Output: 6

// 5. Use Math.trunc() to remove the decimal part of 8.765
console.log(Math.trunc(8.765));  
// Output: 8

// 6. Find the minimum value from 5, 10, -2, 99, 0
console.log(Math.min(5, 10, -2, 99, 0));  
// Output: -2

// 7. Find the maximum value from 23, 89, 12, 78
console.log(Math.max(23, 89, 12, 78));  
// Output: 89

// 8. Find the square root of 81
console.log(Math.sqrt(81));  
// Output: 9

// 9. Find 3 raised to the power of 5
console.log(Math.pow(3, 5));  
// Output: 243

// 10. Generate 3 random numbers between 0 and 100
console.log(Math.floor(Math.random() * 101));  
console.log(Math.floor(Math.random() * 101));  
console.log(Math.floor(Math.random() * 101));  
// Output: (will vary each time, e.g. 42, 7, 96)
